/**
 * @file ota.h
 * @brief Заголовочный файл модуля OTA (Over-The-Air) обновлений
 * 
 * Обеспечивает беспроводное обновление прошивки ESP32.
 * 
 * @note Для работы требуется активное Wi-Fi соединение
 */

#ifndef OTA_H
#define OTA_H

// ============================================================================
// ИНИЦИАЛИЗАЦИЯ OTA
// ============================================================================
/**
 * @brief Настройка OTA обновлений
 * 
 * Регистрирует обработчики событий (начало, прогресс, ошибки).
 * Должна вызываться после подключения к Wi-Fi.
 */
void setupOTA();

// ============================================================================
// ОБРАБОТКА OTA
// ============================================================================
/**
 * @brief Обработка OTA-событий
 * 
 * Должна вызываться периодически (например, из задачи FreeRTOS).
 * Проверяет, не поступил ли запрос на обновление прошивки.
 */
void handleOTA();

// ============================================================================
// ЗАДАЧА FREERTOS
// ============================================================================
/**
 * @brief Создание задачи FreeRTOS для OTA
 * 
 * Создаёт выделенную задачу, которая будет постоянно вызывать handleOTA().
 * Рекомендуется вызывать в setup() после инициализации OTA.
 */
void createOTATask();

#endif // OTA_H