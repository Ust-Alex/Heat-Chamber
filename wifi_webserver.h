#ifndef WIFI_WEBSERVER_H
#define WIFI_WEBSERVER_H

#include <Arduino.h>
#include <WiFi.h>
#include <WebSocketsServer.h>
#include <FS.h>

// Явно указываем пространство имён fs
using namespace fs;

// ============================================================================
// ВАЖНО! ПРАВИЛЬНЫЙ ПОРЯДОК ПОДКЛЮЧЕНИЯ БИБЛИОТЕК
// ============================================================================

// 1. Сначала WiFiManager (он тянет стандартный WebServer)
#include <DNSServer.h>
#include <WiFiManager.h>

// 2. Определяем WEBSERVER_H, чтобы ESPAsyncWebServer не создавал свои HTTP-константы
#define WEBSERVER_H

// 3. Теперь подключаем ESPAsyncWebServer — он видит WEBSERVER_H и не конфликтует
#include <ESPAsyncWebServer.h>

// ============================================================================
// КОНСТАНТЫ
// ============================================================================
#define WEBSOCKET_PORT 8080
#define CONFIG_BUTTON_PIN 0   // GPIO0 (кнопка BOOT)

// ============================================================================
// СТРУКТУРА ДАННЫХ ДЛЯ ОТПРАВКИ КЛИЕНТАМ
// ============================================================================
struct WebData {
  float temps[4];        // индексы: 0 - гильза, 1 - 100см, 2 - 75см, 3 - 50см
  int mode;              // режим работы (0 - стабилизация, 1 - рабочий)
  int color;             // цветовой статус (0 - зеленый, 1 - желтый, 2 - красный)
  char timeStr[6];       // время в формате ЧЧ:ММ (с нулевым байтом)
  float baseTemp;        // базовая температура для режима 2 (рабочего)
};

// ============================================================================
// ФУНКЦИИ
// ============================================================================
void initWiFiManager();
void initWebServer();
void initWebSocket();
void initMDNS();
void broadcastData(const WebData& data);
bool hasWebClients();
void taskWiFi(void* pvParameters);

#endif