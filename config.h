#ifndef CONFIG_H
#define CONFIG_H

#include <Arduino.h>
#include <driver/ledc.h>    // Для аппаратного ШИМ
#include <IPAddress.h>      // Для работы с IP-адресами

// ============================================================================
// НАСТРОЙКИ ПИНОВ (ESP32)
// ============================================================================
// Все пины соответствуют ESP32 mini D1
// ----------------------------------------------------------------------------
#define HEATER_PIN        25   // Пин управления нагревателем (ШИМ)
#define ONE_WIRE_BUS      26   // Пин для датчиков температуры DS18B20
#define BTN_UP_PIN        17   // Кнопка увеличения температуры
#define BTN_DOWN_PIN      16   // Кнопка уменьшения температуры
#define BTN_POWER_PIN      2   // Кнопка вкл/выкл системы

// ============================================================================
// ПАРАМЕТРЫ СИСТЕМЫ
// ============================================================================
#define MAX_TEMP          70.0f   // Максимально допустимая температура (°C)
#define PID_INTERVAL      1000    // Интервал вычисления ПИД (мс)
#define TEMP_UPDATE_INTERVAL 1000 // Интервал опроса датчиков (мс)

// ============================================================================
// НАСТРОЙКИ ГРАФИКОВ НА ДИСПЛЕЕ
// ============================================================================
#define GRAPH_START_X     47      // Начало графика по X (пиксели)
#define GRAPH_END_X       127     // Конец графика по X
#define GRAPH_WIDTH       (GRAPH_END_X - GRAPH_START_X + 1)  // Ширина в пикселях (81)

#define GRAPH_SHIFT_INTERVAL_MS 22500 // 30 минут полного экрана
#define GRAPH_DRAW_INTERVAL_MS  1000  // Обновление дисплея раз в секунду

// Диапазон отображения температур на графике
#define GRAPH_TEMP_MIN    10      // Минимум шкалы графиков
#define GRAPH_TEMP_MAX    70      // Максимум шкалы графиков

// ============================================================================
// НАСТРОЙКИ ШИМ (управление нагревателем)
// ============================================================================
// Частота 1 Гц = период 1 секунда, разрешение 10 бит (0-1023)
// ----------------------------------------------------------------------------
#define PWM_FREQ_HZ       1       // Частота ШИМ
#define PWM_RESOLUTION    10      // Разрешение ШИМ (бит)
#define PWM_CHANNEL       LEDC_CHANNEL_0  // Канал ШИМ
#define PWM_TIMER         LEDC_TIMER_0    // Таймер ШИМ
#define MAX_DUTY          ((1 << PWM_RESOLUTION) - 1)  // Максимальное заполнение (1023)

// ============================================================================
// КОЭФФИЦИЕНТЫ ПИД-РЕГУЛЯТОРА (начальные)
// ============================================================================
#define PID_KP            7.0
#define PID_KI            0.4
#define PID_KD            1.2

// ============================================================================
// ПАРАМЕТРЫ ДАТЧИКОВ
// ============================================================================
#define MAX_SENSORS       3       // Ровно 3 датчика DS18B20
// ВАЖНО: sensorTemps[0] - главный (для ПИД-регулировки)
//         sensorTemps[1] и [2] - информационные

// ============================================================================
// НАСТРОЙКИ WiFi (СТАТИЧЕСКИЙ IP)
// ============================================================================
#define WIFI_SSID         "ZTE-UST"           // Имя твоей WiFi сети
#define WIFI_PASS         "1456654123"        // Пароль от WiFi

// Статические IP-адреса
#define WIFI_IP           IPAddress(192, 168, 100, 50)   // Адрес ESP32
#define WIFI_GATEWAY      IPAddress(192, 168, 100, 1)    // Твой роутер
#define WIFI_SUBNET       IPAddress(255, 255, 255, 0)    // Маска подсети

// ============================================================================
// НАСТРОЙКИ ВЕБ-СЕРВЕРА
// ============================================================================
#define WEBSOCKET_PORT    8080    // Порт для WebSocket
#define WEB_SERVER_PORT   80      // Порт для HTTP

// ============================================================================
// НАСТРОЙКИ ОТЛАДКИ
// ============================================================================
#define DEBUG_SERIAL      1       // 1 - подробный вывод, 0 - только ошибки

#endif // CONFIG_H