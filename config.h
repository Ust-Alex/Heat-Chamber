// ============================================================================
// config.h - Все настройки системы управления нагревателем
// ============================================================================

#ifndef CONFIG_H
#define CONFIG_H

#include <Arduino.h>
#include <driver/ledc.h>

// ============================================================================
// НАСТРОЙКИ ПИНОВ (ESP32)
// ============================================================================
#define HEATER_PIN        25   // Пин управления нагревателем (ШИМ)
#define ONE_WIRE_BUS      26   // Пин для датчиков температуры DS18B20
#define BTN_UP_PIN        17   // Кнопка увеличения температуры
#define BTN_DOWN_PIN      16   // Кнопка уменьшения температуры
#define BTN_POWER_PIN      2   // Кнопка вкл/выкл системы

// ============================================================================
// ПАРАМЕТРЫ СИСТЕМЫ
// ============================================================================
#define MAX_TEMP          70.0f   // Максимально допустимая температура (°C)
#define PID_INTERVAL      1000    // Интервал вычисления ПИД (мс)
#define TEMP_UPDATE_INTERVAL 1000 // Интервал опроса датчиков (мс)

// ============================================================================
// НАСТРОЙКИ ГРАФИКОВ НА ДИСПЛЕЕ
// ============================================================================
#define GRAPH_START_X     47      // Начало графика по X (пиксели)
#define GRAPH_END_X       127     // Конец графика по X
#define GRAPH_WIDTH       (GRAPH_END_X - GRAPH_START_X + 1)  // Ширина в пикселях

// Интервал сдвига графика (время полного экрана)
// 22500 мс = 30 минут (при ширине 81 пиксель)
#define GRAPH_SHIFT_INTERVAL_MS 22500

// Интервал обновления дисплея (мс)
#define GRAPH_DRAW_INTERVAL_MS  1000

// Диапазон отображения температур на графике
#define GRAPH_TEMP_MIN    10
#define GRAPH_TEMP_MAX    70

// ============================================================================
// НАСТРОЙКИ ШИМ (управление нагревателем)
// ============================================================================
#define PWM_FREQ_HZ       1       // Частота 1 Гц = период 1 секунда
#define PWM_RESOLUTION    10      // 10 бит (0..1023)
#define PWM_CHANNEL       LEDC_CHANNEL_0
#define PWM_TIMER         LEDC_TIMER_0
#define MAX_DUTY          ((1 << PWM_RESOLUTION) - 1)  // = 1023

// ============================================================================
// КОЭФФИЦИЕНТЫ ПИД-РЕГУЛЯТОРА (начальные)
// ============================================================================
#define PID_KP            7.0
#define PID_KI            0.4
#define PID_KD            1.2

// ============================================================================
// ПАРАМЕТРЫ ДАТЧИКОВ
// ============================================================================
#define MAX_SENSORS       3       // Максимальное количество датчиков

#endif // CONFIG_H